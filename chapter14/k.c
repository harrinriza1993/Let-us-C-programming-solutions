/*(k) A 6 x 6 matrix is entered through the keyboard. Write a program
      to obtain the Determinant value of the matrix.

Apporach:
	1. 

*/

#include<stdio.h>

void main()
{
	int a[6][6]; 
	int size = 6, i, j;
	
	printf("Enter the numbers\n");

	for(i = 0; i < size; i++)
	{
		for(j = 0; j < size; j++)
		{
			scanf("%d",&a[i][j]);
		}
	}
}
	
	
		  

	
	
